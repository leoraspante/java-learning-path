// Entity class representing a salary value.

package model.entities;

public class Salary {

	// Fields.
	private double value;

	// Constructor.
	public Salary(double value) {
		this.value = value;
	}

	// Getters and Setters.
	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}
	
}
