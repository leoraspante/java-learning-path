//  Entity class representing a circle.

package model.entities;

public class Circle {

	// Fields.
	private double radius;

	// Constructor.
	public Circle(double radius) {
		this.radius = radius;
	}

	// Getters and Setters.
	public double getRadius() {
		return radius;
	}

	public void setRadius(double radius) {
		this.radius = radius;
	}
}
