// Entity class representing a number value.

package model.entities;

public class NumberValue {

	// Fields.
	private Integer value;

	// Constructor.
	public NumberValue(Integer value) {
		this.value = value;
	}

	// Getters and Setters.
	public Integer getValue() {
		return value;
	}

	public void setValue(Integer value) {
		this.value = value;
	}
	
}
